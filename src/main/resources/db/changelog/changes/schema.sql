CREATE TABLE users (
  id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  username TEXT NOT NULL UNIQUE CHECK (
    LENGTH(username) > 4
    AND LENGTH(username) < 32
    AND username ~ '^[a-zA-Z0-9_-]+$'
  ),
  password TEXT NOT NULL CHECK (
    LENGTH(password) = 60
    AND password ~ '^[[:print:]]+$'
  ),
  role TEXT NOT NULL DEFAULT 'executor',
  balance NUMERIC(16, 2) NOT NULL DEFAULT 0,
  is_blocked BOOLEAN NOT NULL DEFAULT FALSE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
CREATE TABLE tasks (
  id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  description TEXT NOT NULL,
  customer_id BIGINT NOT NULL REFERENCES users (id),
  status TEXT NOT NULL DEFAULT 'open',
  accepted_bid_id BIGINT,
  reward NUMERIC(16, 2) NOT NULL CHECK (reward > 0),
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
CREATE TABLE bids (
  id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  executor_id BIGINT NOT NULL REFERENCES users (id),
  task_id BIGINT NOT NULL REFERENCES tasks (id),
  description TEXT NOT NULL,
  status TEXT NOT NULL DEFAULT 'pending',
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
ALTER TABLE
  tasks
ADD
  CONSTRAINT fk_tasks_accepted_bid_id FOREIGN KEY (accepted_bid_id) REFERENCES bids (id);
CREATE UNIQUE INDEX idx_bids_one_pending_per_user_task ON bids (executor_id, task_id)
WHERE
  status = 'pending';
CREATE UNIQUE INDEX idx_bids_one_accepted_per_task ON bids (task_id)
WHERE
  status = 'accepted';
CREATE TABLE payment (
  id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  task_id BIGINT NOT NULL UNIQUE REFERENCES tasks (id),
  bid_id BIGINT NOT NULL REFERENCES bids (id),
  amount NUMERIC(16, 2) NOT NULL CHECK (amount > 0),
  processed_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
CREATE INDEX idx_tasks_status ON tasks (status);
CREATE INDEX idx_tasks_customer ON tasks (customer_id);
CREATE INDEX idx_bids_task ON bids (task_id);
CREATE INDEX idx_bids_executor ON bids (executor_id);
CREATE INDEX idx_users_role ON users (role);
