CREATE TYPE user_role AS ENUM('admin');
CREATE DOMAIN currency AS numeric(16, 2);
CREATE TYPE bids_status AS ENUM(
	'pending', 'rejected', 'accepted',
	'cancelled'
);

CREATE TABLE IF NOT EXISTS users (
	id bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	username text NOT NULL UNIQUE CHECK (
		LENGTH(username) > 4
		AND LENGTH(username) < 32
		AND username ~ '^[a-zA-Z0-9]$'
	),
	password text NOT NULL CHECK (
		LENGTH(username) = 60
		AND password ~ '^[[:print:]]+$'
	),
	balance currency NOT NULL DEFAULT 0 CHECK (balance >= 0)
);

CREATE TABLE IF NOT EXISTS tasks (
	id bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	description text NOT NULL,
	customer_id bigint NOT NULL,
	accepted_bid_id bigint,
	reward currency NOT NULL CHECK (reward > 0),
	CONSTRAINT fk_tasks_customer_id FOREIGN KEY (customer_id) REFERENCES users (id)
	--		CONSTRAINT fk_tasks_accepted_bid_id
	--		FOREIGN KEY (accepted_bid_id) REFERENCES bids (id)
	);

CREATE TABLE IF NOT EXISTS bids (
	id bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	executor_id bigint NOT NULL,
	task_id bigint NOT NULL,
	description text NOT NULL,
	status bids_status NOT NULL,
	CONSTRAINT fk_bids_executor_id FOREIGN KEY (executor_id) REFERENCES users (id),
	CONSTRAINT fk_bids_task_id FOREIGN KEY (task_id) REFERENCES tasks (id)
);

